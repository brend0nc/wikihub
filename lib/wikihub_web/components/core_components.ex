  """
  attr :name, :string, required: true
  attr :class, :any, default: "size-4"

  def icon(%{name: "hero-" <> _} = assigns) do
    ~H"""
    <span class={[@name, @class]} />
    """
  end

  ## JS Commands

  def show(js \\ %JS{}, selector) do
    JS.show(js,
      to: selector,
      time: 300,
      transition:
        {"transition-all ease-out duration-300",
         "opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95",
         "opacity-100 translate-y-0 sm:scale-100"}
    )
  end

  def hide(js \\ %JS{}, selector) do
    JS.hide(js,
      to: selector,
      time: 200,
      transition:
        {"transition-all ease-in duration-200", "opacity-100 translate-y-0 sm:scale-100",
         "opacity-0 translate-y-4 sm:translate-y-0 sm:scale-95"}
    )
  end

  @doc """
  Translates an error message using gettext.
  """
  def translate_error({msg, opts}) do
    Enum.reduce(opts, msg, fn {key, value}, acc ->
      String.replace(acc, "%{#{key}}", fn _ -> to_string(value) end)
    end)
  end

  @doc """
  Translates the errors for a field from a keyword list of errors.
  """
  def translate_errors(errors, field) when is_list(errors) do
    for {^field, {msg, opts}} <- errors, do: translate_error({msg, opts})
  end

  ## Wikihub shared chrome -----------------------------------------------------

  @doc """
  Page shell shared by every view: sticky masthead (wordmark + global search +
  live counts) and a wiki sidebar, wrapping each view's content.
  """
  attr :view, :string, required: true, doc: "dashboard | reader | graph"
  attr :wikis, :list, required: true, doc: "all wikis, for the sidebar + counts"
  attr :active_wiki, :string, default: nil
  attr :ts, :any, default: nil, doc: "last refresh DateTime"
  attr :q, :string, default: ""
  attr :results, :map, default: nil
  slot :inner_block, required: true

  def hub_shell(assigns) do
    ~H"""
    <div class={["hub-shell", "view-#{@view}"]}>
      <.masthead wikis={@wikis} ts={@ts} q={@q} results={@results} />
      <div class="hub-body">
        <.wiki_sidebar wikis={@wikis} active_wiki={@active_wiki} ts={@ts} />
        <main class="hub-main">
          {render_slot(@inner_block)}
        </main>
      </div>
    </div>
    """
  end

  attr :wikis, :list, required: true
  attr :ts, :any, default: nil
  attr :q, :string, default: ""
  attr :results, :map, default: nil

  def masthead(assigns) do
    ~H"""
    <header class="masthead">
      <.link navigate={~p"/"} class="brand">Wiki Hub</.link>
      <form
        class="mast-search"
        phx-change="global_search"
        phx-submit="global_search"
        autocomplete="off"
      >
        <input
          id="site-search"
          type="text"
          name="q"
          value={@q}
          class="search-input"
          placeholder="Search all wikis…   press /"
          phx-debounce="120"
          phx-hook="GlobalSearch"
        />
        <.search_overlay :if={@results} results={@results} />
      </form>
      <span class="counts">
        {length(@wikis)} wikis · {Enum.sum(Enum.map(@wikis, &length(&1.pages)))} pages · {fmt_ts(@ts)}
      </span>
    </header>
    """
  end

  attr :results, :map, required: true

  def search_overlay(assigns) do
    ~H"""
    <div class="search-overlay" id="search-overlay">
      <%= if @results.wikis == [] and @results.pages == [] do %>
        <div class="search-empty">No matches.</div>
      <% else %>
        <%= if @results.wikis != [] do %>
          <div class="search-group">Wikis</div>
          <.link :for={w <- @results.wikis} navigate={~p"/r/#{w.name}"} class="search-hit">
            <span class="sh-title">{w.name}</span>
            <span class="sh-meta">· {length(w.pages)} pages</span>
          </.link>
        <% end %>
        <%= if @results.pages != [] do %>
          <div class="search-group">Pages</div>
          <.link :for={{wn, p} <- @results.pages} navigate={~p"/r/#{wn}/#{p.id}"} class="search-hit">
            <span class="sh-title">{p.title}</span>
            <span class="sh-meta">· {wn} / {p.category}</span>
          </.link>
        <% end %>
      <% end %>
    </div>
    """
  end

  attr :wikis, :list, required: true
  attr :active_wiki, :string, default: nil
  attr :ts, :any, default: nil

  def wiki_sidebar(assigns) do
    ~H"""
    <nav class="wiki-sidebar">
      <div class="side-label">Wikis</div>
      <%= for w <- Enum.sort_by(@wikis, & &1.name) do %>
        <.link
          navigate={~p"/r/#{w.name}"}
          class={["side-wiki", w.name == @active_wiki && "is-active"]}
        >
          <span class={"dot #{w.bucket}"}>{dot(w.bucket)}</span>
          {w.name}
          <span class="side-count">{length(w.pages)}</span>
        </.link>
        <.link :if={w.name == @active_wiki} navigate={~p"/g/#{w.name}"} class="side-sub muted">
          floor plan →
        </.link>
      <% end %>
      <div :if={@wikis == []} class="side-label muted">none found</div>
      <div class="side-footer">
        refreshed {fmt_ts(@ts)}<br />looking in <code>{wiki_paths_label()}</code>
      </div>
    </nav>
    """
  end

  defp dot(:fresh), do: "●"
  defp dot(:aging), do: "◐"
  defp dot(:stale), do: "○"
  defp dot(_), do: "·"

  defp fmt_ts(%DateTime{} = ts), do: Calendar.strftime(ts, "%H:%M UTC")
  defp fmt_ts(_), do: "—"

  defp wiki_paths_label do
    case Application.get_env(:wikihub, :wiki_paths) do
      blank when blank in [nil, ""] -> "~/*/wiki"
      paths -> paths
    end
  end
end
